    private void jButton_show_table_detailsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_show_table_detailsActionPerformed
        // TODO add your handling code here:
        
        try{
           Connection con;
           con = DriverManager.getConnection("jdbc:mysql://localhost:3307/itpmnew","root","");
                   
                    
            Statement st = con.createStatement();
            
           //mySql query
           String sql = "SELECT * FROM `session`";
           ResultSet rs = st.executeQuery(sql);
           
           while(rs.next()){
               //data will be added until finish session table data
               String session_ID = String.valueOf(rs.getInt("session_ID"));
               String lec1 = rs.getString("lec1");
               String lec2 = rs.getString("lec2");
               String sub_code = rs.getString("sub_code");
               String sub_name = rs.getString("sub_name");
               String stu_count = String.valueOf(rs.getInt("stu_count"));
               String tag = rs.getString("tag");
               String duration = String.valueOf(rs.getInt("duration"));
               String maingroup = rs.getString("Maingroup");
               String subgroup = rs.getString("Subgroup");
               String full_grpID = rs.getString("full_grpID");
              
               //String array for data into jTable
               String session[] = {session_ID,lec1,lec2,sub_code,sub_name,stu_count,tag,duration,maingroup,subgroup,full_grpID};
               
               //DefaultTableModel tblModel = (DefaultTableModel)jTable_SessionsT.getModel();
               DefaultTableModel tblModel = (DefaultTableModel)jTable_Sessions.getModel();
               //add string array data in to jTable
               tblModel.addRow(session);
                       }
           con.close();
       }catch(Exception e){
           e.printStackTrace();
            
            
        
    }                                        
    }//GEN-LAST:event_jButton_show_table_detailsActionPerformed

